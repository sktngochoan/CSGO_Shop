    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
            int id = Integer.parseInt(request.getParameter("id"));
        User u = new UserDAO().getUserById(id);
        String [] email = u.getUser_Email().split("@");
        if(u != null){
            request.setAttribute("user", u);
        }
        request.setAttribute("email", email[0]);
        request.getRequestDispatcher("editprofile.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User a = (User)session.getAttribute("account");
        String name = request.getParameter("name");
        String email = request.getParameter("email");
        String phone = request.getParameter("phone");
        String linkSteam = request.getParameter("linkSteam");
        
        User edit = new User(a.getUser_Id(), name,a.getUser_Password(), email, phone, linkSteam, a.isUser_Role());
        UserDAO.editUser(edit);
        session.removeAttribute("account");
        session.setAttribute("account", edit);
        response.sendRedirect("home");
    }

