    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        List<Skin> listnewestSkin = new ProductDAO().getNewestSkin();
        List<Skin> listTop3Skin = new ProductDAO().getListTop3Skin();
        List<Category> listCate = new CategoryDAO().getAllCategory();
        request.setAttribute("listnewestSkin", listnewestSkin);
        request.setAttribute("listTop3Skin", listTop3Skin);
        session.setAttribute("listCate", listCate);
        request.getRequestDispatcher("home.jsp").forward(request, response);
    }

