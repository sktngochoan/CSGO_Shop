    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        new ChartDAO().UpdateStatistic(3);
        Order order = new OrderDAO().getOrder();
        List<OrderDetail> listOrderDetails = new OrderDetailDAO().getOrder(order.getOrderId());
        double totalPrice = 0;
        for(int i = 0;i<listOrderDetails.size();i++){
            totalPrice+=listOrderDetails.get(i).getSkinPrice();
        }
        request.setAttribute("date", java.time.LocalDate.now());
        request.setAttribute("list", listOrderDetails);
        request.setAttribute("totalPrice", Math.round(totalPrice*100)/100);
        request.getRequestDispatcher("checkout.jsp").forward(request, response);
    }

